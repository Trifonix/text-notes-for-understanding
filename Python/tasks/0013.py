""" Комплексная задача: «Терминал кафе»
Напиши скрипт, который выполняет следующие требования:

1. Комментарии
* В самом начале файла добавь блочный комментарий (или docstring в тройных кавычках), кратко описывающий назначение программы.
* Добавь 1–2 понятных inline-комментария (через #) к блокам с математическими расчетами (не нужно комментировать очевидные input или print).

2. Ввод данных (Input)
Запроси у пользователя через консоль:
* Имя клиента (str).
* Название заказанного блюда (str).
* Количество порций (целое число int).
* Цену за одну порцию (дробное число float).
* Текущий баланс бонусов клиента (целое число int).

3. Переменные и Расчеты
* Все переменные должны быть названы строго по стандарту PEP 8 (snake_case).
* Посчитай общую стоимость покупки (количество $\times$ цена за порцию).
* Посчитай новые бонусы: клиенту начисляется 10% от суммы покупки (переведи полученное значение в целое число с помощью int()).
* Обнови баланс бонусов клиента, прибавив новые бонусы к старым (обязательно используй сокращенный оператор +=).

4. Вывод данных (Output)
Выведи на экран чек. В выводе должны быть использованы:
* f-строки для формирования текста.
* Вывод общей суммы с ровно 2 знаками после запятой (:.2f).
* Использование аргументов sep или end в одном из вызовов print() (например, для красивой рамки или завершающей подписи).

Пример работы программы (вывод в консоли):
=== ЧЕК ПОКУПКИ ===
Клиент: Алексей
Товар: Капучино x 2
Итого к оплате: 450.00 руб.
--------------------
Начислено бонусов: 45
Ваш новый баланс: 145 бонусов!
====================
"""

'''
Программа для подсчёта итогового чека с данными клиента
'''

client_name = input("Как Вас зовут? ")
client_food = input("Какое блюды Вы заказали? ")
client_food_count = int(input(f"Сколько порций `{client_food}` Вы заказали? "))
client_food_price = float(input(f"Сколько стоит 1 порция `{client_food}`? "))
client_bonuses = int(input("Сколько у Вас бонусов? "))

# Общая стоимость блюда с учётом порций 
final_price = client_food_count * client_food_price
# Клиент получает 10% бонусов от общей стоимости блюда
new_bonuses = int(final_price * 0.1)
client_bonuses += new_bonuses

print("\n=== ЧЕК ПОКУПКИ ===")
print(
    f"Клиент: {client_name}", 
    f"Товар: {client_food} x {client_food_count}",
    f"Итого к оплате: {final_price:.2f}",
    sep=" | ", end="\n--------------------\n"
)
print(f"Начислено бонусов: {new_bonuses}\nВаш новый баланс: {client_bonuses} бонусов!", end="\n====================\n")



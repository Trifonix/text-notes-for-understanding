""" Задача 2: «Мастер настройки print()» (Средняя)
Функция print() по умолчанию разделяет аргументы пробелом (sep=' ') и переносит строку в конце (end='\n'). Но их можно менять!
Твое задание:
Запроси у пользователя по очереди три разных слова (например, город, улицу и номер дома).
Выведи их в одну строку в формате адреса, используя аргумент sep=" -> ".
В конце строки вместо обычного переноса поставь точку и восклицательный знак end=".!\n".
Пример вывода: Москва -> Тверская -> 12.!
"""

city = input("Введите город: ")
street = input("Введите улицу: ")
house_number = int(input("Введите номер дома: "))

print(city, street, house_number, sep=" -> ", end=".!\n")



""" Задача 3: «Делим чек в ресторане» (Сложнее)
Здесь потребуется работа с дробными числами (float) и красивым форматированием вывода.
Твое задание:
Напиши калькулятор для компании друзей:
Запроси общую сумму чека (дробное число, например 3500.50).
Запроси процент чаевых (например 10 или 15).
Запроси количество человек (целое число).
Посчитай:
* Итоговую сумму с учетом чаевых.
* Сколько должен заплатить каждый человек.
Выведи результат. Форматируй числа так, чтобы у них было ровно 2 знака после запятой (используй спецификатор :.2f внутри f-строки, например: {total:.2f}).
"""

check = float(input("Общая сумма чека: "))      # 1000
for_tea = int(input("На чай в процентах: "))    # 10
eaters = int(input("Количество человек: "))     # 2

final_give = check * (1 + for_tea / 100)        # 1100
single_give = final_give / eaters               # 550

print(f"Всего нужно отдать: {final_give:.2f}, с каждого по: {single_give:.2f}")



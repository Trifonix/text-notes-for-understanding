# Математика: Формула check * (1 + for_tea / 100) — отдельный зачет! Написать так намного элегантнее, чем разворачивать в check + (check * for_tea / 100).

# Форматирование: Спецификатор :.2f применен точно по назначению. Теперь вывод всегда будет аккуратным (например, 3500.50 вместо 3500.5000000000005).

